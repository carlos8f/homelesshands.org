#!/bin/bash
#
# Allow Rackspace managed backup (MBU) traffic.
#

IPTABLES=/sbin/iptables

$IPTABLES -N RACKSPACEIPS
$IPTABLES -A RACKSPACEIPS -i eth1 -p tcp -s 10.251.0.0/16 -j ACCEPT
$IPTABLES -A RACKSPACEIPS -i eth1 -p tcp -s 10.191.0.0/16 -j ACCEPT
$IPTABLES -A RACKSPACEIPS -s 64.39.0.0/23 -j ACCEPT
$IPTABLES -A RACKSPACEIPS -s 64.39.2.144/28 -j ACCEPT
$IPTABLES -A RACKSPACEIPS -s 212.100.225.32/27 -j ACCEPT
$IPTABLES -A RACKSPACEIPS -s 10.0.0.0/12 -j ACCEPT
$IPTABLES -I INPUT 1 -j RACKSPACEIPS
$IPTABLES -I OUTPUT -o eth1 -p tcp -d 10.251.0.0/16 -j ACCEPT
$IPTABLES -I OUTPUT -o eth1 -p tcp -d 10.191.0.0/16 -j ACCEPT

